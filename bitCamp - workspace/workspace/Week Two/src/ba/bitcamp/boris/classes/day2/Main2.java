package ba.bitcamp.boris.classes.day2;

public class Main2 {

	public static void main(String[] args) {
		int counter = 0;
		while (counter < 6) {
			System.out.println(counter);
			counter++;
		}

		// while ili for
		
		for (int i = 6; i > 0; i--) { // svaki slijedeci put se ne izvrsi i = 0, samo prvi put
			System.out.println(i);
		}
		
		
	}

}
