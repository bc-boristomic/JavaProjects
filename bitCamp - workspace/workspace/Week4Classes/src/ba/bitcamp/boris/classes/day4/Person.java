package ba.bitcamp.boris.classes.day4;

public class Person {
	
	public String firstName;
	public String lastName;
	public Integer yearOfBirth;
	public String address;
	public String personalID;
	public String email;
	public Integer height;
	public Boolean isValid;
	public String favFood;
	public String favDrink;
	
	
	
	public Person(String firstName, String lastName, Integer yearOfBirth,
			String address, String personalID, String email, Integer height,
			Boolean isValid, String favFood, String favDrink) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.yearOfBirth = yearOfBirth;
		this.address = address;
		this.personalID = personalID;
		this.email = email;
		this.height = height;
		this.isValid = isValid;
		this.favFood = favFood;
		this.favDrink = favDrink;
	}
	
	
	
	
	

}
