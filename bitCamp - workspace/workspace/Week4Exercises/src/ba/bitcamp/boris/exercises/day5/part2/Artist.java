package ba.bitcamp.boris.exercises.day5.part2;

public class Artist {

	public String name;
	public boolean isBand;
	public int activeSince;
	public String genre;
	
	
	
	public Artist(String name, boolean isBand, int activeSince, String genre) {
		this.name = name;
		this.isBand = isBand;
		this.activeSince = activeSince;
		this.genre = genre;
	}
	
	
	

}

